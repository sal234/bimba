         biMBA-AIUEO adalah program Minat Baca dan Minat Belajar Anak yang berfokus pada proses, bukan pada hasil. Terbukti metode ini lebih berhasil meningkatkan kemampuan baca dan belajar anak daripada metode yang menekankan pada hasil. Keistimewaan biMBA-AIUEO adalah Fun Learning, Small Step System dan Individual System
         
         
<h2> Garansi BCA-372/BEBAS</h2>
        

<p>Surat jaminan tertulis atau garansi Bisa baCA kata sederhana bagi anak usia 3 tahun 0 bulan dalam waktu 72 jam belajar di kelas meskipun TANPA harus dibantu orangtua di rumah (BEBAS). Jika dalam waktu 72 jam anak belum mampu membaca kata sederhana, maka anak akan dibimbing secara intensif sampai bisa baca kata sederhana yang digaransikan secara gratis dengan memenuhi syarat dan ketentuan yang berlaku.
              
              Coba Gratis Sampai Yakin
              
              Coba Gratis Sampai Yakin merupakan suatu fasilitas yang diberikan biMBA-AIUEO kepada orangtua murid untuk coba gratis sampai yakin mau menitipkan anaknya.              </p>
<p>
  <h2>biMBA-AIUEO</h2>
berada dalam naungan Yayasan Pengembangan Anak Indonesia yang berdiri tahun 1996. Kami bukan tempat les atau kursus membaca, karena yang kami tumbuhkan adalah Minat atau keinginan anak untuk baca dan belajar, bukan karena paksaan orangtua ataupun guru, melainkan karena anak ingin dan senang melakukannya. Dampak dari tumbuhnya Minat itu adalah meningkatnya kemampuan baca dan belajar anak secara LUAR BIASA.
              
              Mengapa demikian? Bila anak mendapat kesan positif bahwa belajar itu menyenangkan, pasti ia akan mau mengulanginya lagi dengan senang hati, kapan pun dan di mana pun tanpa merasa terbebani (The Law of Effect). Pengulangan tersebut merupakan latihan bagi anak, dengan seringnya anak melakukan latihan maka kemampuannya akan semakin meningkat (The law Of Exercise).
              Jika anak mampu, maka kegiatan belajar akan menjadi menyenangkan (The law of Readiness). </p>
<h2>Tujuan biMBA</h2>

Tujuan Khusus:

Meningkatkan MINAT baca dan belajar anak secara intrinsik. Anak mau melakukan kegiatan baca dan belajar karena keinginannya sendiri bukan karena paksaan, sehingga setiap anak merasa BAHAGIA bukan terbebani, yang akan berdampak pada meningkatnya kemampuan anak. Inilah ke-SUKSES-an yang dicapai anak.

Tujuan Umum:

Seluruh keluarga besar biMBA merasa BAHAGIA & SUKSES.

         
